$("#connexion").click(function(){

	if ($("#form-connexion").css("display","none")) {
            $("#form-connexion").slideDown(100).css("display","block")
    }
});

$("#connexion").click(function(){

	if ($("#form-connexion").css.display != 'none') {
            $("#form-connexion").slideDown(100).css("display","none")
    }
});